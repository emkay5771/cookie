"""INSTRUCTIONS"""

"""Preheat the oven to 350F and line a large baking sheet with parchment paper."""
"""Separate the packs of chocolate chips, toffee and Maldon sea salt from the dry mix. In a medium sized bowl, add the melted and cooled butter, brown sugar, and sugar. It’s important that the butter is not hot, but it can be slightly warm. Whisk vigorously for 1-2 minutes until it turns into a paste. Whisk in the egg until smooth. Add in the dry mix and use a rubber spatula to fold to combine. Leave a few streaks of flour and pour in the chocolate chips and toffee pieces. Continue to fold the dough until the chocolate chips and toffee are dispersed and the flour is completely blended in. Do not over mix."""
"""A large (2oz) cookie scoop (or 1/4 measuring cup) is preferred for this recipe. Scoop 5 cookies onto the baking sheet and bake for 12-15 minutes."""
"""The cookies are ready when they have a golden ring around the edge but are pale and puffed in the center.""" 
"""Sprinkle with flaky salt and let cool 20 minutes (on a rack) before enjoying."""

